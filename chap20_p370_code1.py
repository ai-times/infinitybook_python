from datetime import datetime 
birth = datetime(2002, 6, 30, 10, 15, 3, 100) 

print("태어난 년도:", birth.year ) 
print("태어난 달:", birth.month ) 
print("태어난 날:", birth.day )
print("태어난 시간:", birth.hour )
print("태어난 분:", birth.minute )
print("태어난 초:", birth.second )
print("태어난 마이크로초:", birth.microsecond )
